export const initializeSampleUsers = () => {
  const existingUsers = localStorage.getItem('masterAdminUsers');
  if (!existingUsers) {
    const usersWithIds = sampleUsers.map(user => ({
      ...user,
      id: crypto.randomUUID(),
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString(),
    }));
    localStorage.setItem('masterAdminUsers', JSON.stringify(usersWithIds));
  }
};

